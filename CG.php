        <p>Computer graphics are pictures and films created using computers. Usually, the term refers to computer-generated image data created with the help of specialized graphical hardware and software. It is a vast and recently developed area of computer science. The phrase was coined in 1960, by computer graphics researchers Verne Hudson and William Fetter of Boeing. It is often abbreviated as CG, though sometimes erroneously referred to as computer-generated imagery (CGI).

Some topics in computer graphics include user interface design, sprite graphics, rendering, geometry processing, computer animation, vector graphics, 3D modeling, shaders, GPU design, implicit surface visualization with ray tracing, image processing, computational photography, scientific visualization, and computer vision, among others. The overall methodology depends heavily on the underlying sciences of geometry, optics, physics, and perception.


